    void LoadData(ArrayList<ChucVuViewModel> cv){
        dtm = (DefaultTableModel) tblChucVU.getModel();
        dtm.setRowCount(0);
        for (ChucVuViewModel x : cv) {
            dtm.addRow(new Object[]{x.getId(),x.getMa(),x.getTen()});
            
        }
    }
