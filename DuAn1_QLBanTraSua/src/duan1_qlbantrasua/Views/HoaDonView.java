    private void btnLoc1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoc1ActionPerformed
        if(chkTienMat.isSelected() && chkChuyenKhoan.isSelected()){
            listDanhSachHD = hds.timTheoHinhThucThanhToan(2);
            loadTable(listDanhSachHD);
        }else if(chkTienMat.isSelected()){
            listDanhSachHD = hds.timTheoHinhThucThanhToan(0);
            loadTable(listDanhSachHD);
        }else if(chkChuyenKhoan.isSelected()){
            listDanhSachHD = hds.timTheoHinhThucThanhToan(1);
            loadTable(listDanhSachHD);
        }else{
            JOptionPane.showMessageDialog(this, "Chưa chọn loại hình thanh toán!");
            return;
        }
    }//GEN-LAST:event_btnLoc1ActionPerformed

