    private void loadTableDS() {
        DefaultTableModel model = (DefaultTableModel) this.tblDSHD.getModel();
        model.setRowCount(0);
        for (DanhSachHoaDon ds : this.hds.getListDS()) {
            Object[] rowData = {
                ds.getMaHD(),
                ds.getNguoiTao(),
                ds.getKhachHang(),
                ds.getNgayTao(),
                ds.getNgayThanhToan(),
                ds.getTongTien(),
                ds.getGhiChu(),
                ds.getTrangThai() == 0 ? "Chờ thanh toán" : ds.getTrangThai() == 1 ? "Đã thanh toán" : "Hủy thanh toán",
            };

            model.addRow(rowData);
        }
    }

    private void loadTableCT(String ma) {
        DefaultTableModel model = (DefaultTableModel) this.tblCTHD.getModel();
        model.setRowCount(0);
        for (ChiTietHoaDon ct : this.hds.getListCTTheoMa(ma)) {
            Object[] rowData = {
                ct.getMa(),
                ct.getTen(),
                ct.getGia(),
                ct.getSoLuong(),
                ct.getMucDuong(),
                ct.getMucDa(),
                ct.getTopping(),
                ct.getThanhTien(ct.getSoLuong(), ct.getGia()),
                ct.getTrangThai()
            };

            model.addRow(rowData);
        }
    }

    private void loadTable(ArrayList<DanhSachHoaDon> listDanhSachHD) {
        DefaultTableModel dtm = (DefaultTableModel) this.tblDSHD.getModel();
        dtm.setRowCount(0);
        for (DanhSachHoaDon hoaDon : listDanhSachHD) {
            dtm.addRow(new Object[]{
                hoaDon.getMaHD(),
                hoaDon.getNguoiTao(),
                hoaDon.getKhachHang(),
                hoaDon.getNgayTao(),
                hoaDon.getNgayThanhToan(),
                hoaDon.getTongTien(),
                hoaDon.getGhiChu(),
                hoaDon.getTrangThai()==0?"Chưa thanh toán":hoaDon.getTrangThai()==1?"Đã thanh toán":"Đã hủy"
            });
        }
    }

    public void loc(String txt) {
        DefaultTableModel m = (DefaultTableModel) tblDSHD.getModel();
        m.fireTableDataChanged();
        TableRowSorter sorter = new TableRowSorter(m);
        tblDSHD.setRowSorter(sorter);
        sorter.setRowFilter(RowFilter.regexFilter(txt));
    }

    private void loadTableCT(ArrayList<CTGioHang> listCTGH) {
        DefaultTableModel dtm = (DefaultTableModel) tblCTHD.getModel();
        dtm.setRowCount(0);
        int sTT = 1;
        for (CTGioHang cTGioHang : listCTGH) {
            dtm.addRow(new Object[]{
                sTT++,
                cTGioHang.getMaHoaDon(),
                cTGioHang.getTenSanPham(),
                cTGioHang.getSoLuongSP(),
                cTGioHang.getDongGia(),
                cTGioHang.getTenDa(),
                cTGioHang.getTenDuong(),
                cTGioHang.getTenTopping(),
                cTGioHang.getTongTien()
            });
        }
    }
    
