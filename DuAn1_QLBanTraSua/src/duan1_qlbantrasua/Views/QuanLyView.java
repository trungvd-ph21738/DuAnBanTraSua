    private void btnThemMdgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMdgActionPerformed
        MucDuong dg = this.getFormDataDuong();
        if (dg == null) {
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không?");
        if (confirm == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, mdgs.themMucDuong(dg));
            clearFormDuong();
            listMucDuong = mdgs.getListMucDuongDB();
            loadTableMucDuong();
        }
    }//GEN-LAST:event_btnThemMdgActionPerformed

    private void btnSuaMdgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaMdgActionPerformed
        int row = tblDuong.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn một dòng để sửa!");
            return;
        }
        MucDuong mucDuong = this.getFormDataDuong();
        if (mucDuong == null) {
            return;
        }
        String id = lblIdDuong.getText();
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?");
        if (confirm == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, mdgs.updateMucDuong(mucDuong, id));
            clearFormDuong();
            listMucDuong = mdgs.getListMucDuongDB();
            loadTableMucDuong();
        }
    }//GEN-LAST:event_btnSuaMdgActionPerformed

    private void btnXoaMdgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaMdgActionPerformed
        int row = tblDuong.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn một dòng để xóa!");
            return;
        }
        String ma = this.tblDuong.getValueAt(row, 1).toString();
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?");
        if (confirm == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, mdgs.xoaMucDuong(ma));
            clearFormDuong();
            listMucDuong = mdgs.getListMucDuongDB();
            loadTableMucDuong();
        }
    }//GEN-LAST:event_btnXoaMdgActionPerformed

