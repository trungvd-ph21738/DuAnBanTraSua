    private void tblDanhMucMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhMucMouseClicked
        int row = tblDanhMuc.getSelectedRow();
        DanhMucSP danhMuc = qLDanhMuc.getListDanhMucSPDB().get(row);
        lblIDDanhMuc.setText(danhMuc.getId());
        lblMaDanhMuc.setText(danhMuc.getMa());
        txtTenDanhMuc.setText(danhMuc.getTen());
        rdoConBan.setSelected(danhMuc.getTrangThai() == 0?true:false);
        rdoNgungBan.setSelected(danhMuc.getTrangThai() == 1?true:false);
    }//GEN-LAST:event_tblDanhMucMouseClicked

